        mailSender.setHost("smtp.gmail.com");
        mailSender.setPort(587);

        mailSender.setUsername("vijaypratap7705@gmail.com");
        mailSender.setPassword("77viJAY04");


//      property setting for mailSender
        Properties props = mailSender.getJavaMailProperties();
